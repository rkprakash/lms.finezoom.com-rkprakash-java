class ConvertDatatype{
	public static void main(String args[]){
		//Convert Long to String
		long inputValue =23244424;   
		String result= String.valueOf(inputValue);
		System.out.println("Convert long to string value: "+result);
		
		//Convert Float to Integer
		float aNumber = 12.78f;
	    int bNumber = (int) aNumber;
		System.out.println("Convert float to integer value :"+bNumber);
		
	}
}